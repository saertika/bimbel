    private void jButton1TambahTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1TambahTambahActionPerformed
String nama = txtNama.getText();
    String tanggal = comboTanggal.getSelectedItem().toString() + " " +
                     comboBulan.getSelectedItem().toString() + " " +
                     comboTahun.getSelectedItem().toString();
    String paket = comboPaket.getSelectedItem().toString();
    String kelas = cbKelas.getSelectedItem().toString();

    String level = "";
    int hargaLevel = 0;

    if (rbA1.isSelected()) { level = "A1"; hargaLevel = hargaA1; }
    else if (rbA2.isSelected()) { level = "A2"; hargaLevel = hargaA2; }
    else if (rbB1.isSelected()) { level = "B1"; hargaLevel = hargaB1; }
    else if (rbB2.isSelected()) { level = "B2"; hargaLevel = hargaB2; }
    else if (rbB3.isSelected()) { level = "B3"; hargaLevel = hargaB3; }
    else if (rbC1.isSelected()) { level = "C1"; hargaLevel = hargaC1; }

    int hargaKelas = 0;

    switch (kelas.trim()) {
        case "Reguler": hargaKelas = hargaReguler; break;
        case "Intensive": hargaKelas = hargaIntensive; break;
        case "Privat": hargaKelas = hargaPrivat; break;
        case "Weekend": hargaKelas = hargaWeekend; break;
        case "Online": hargaKelas = hargaOnline; break;
    }

    int total = hargaLevel + hargaKelas;

    javax.swing.table.DefaultTableModel model = 
            (javax.swing.table.DefaultTableModel) jTable1.getModel();

    model.addRow(new Object[]{
        nama,
        tanggal,
        paket,
        kelas,
        level,
        total
    });

    javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1TambahTambahActionPerformed

    private void txtSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSearchActionPerformed
String searchText = txtSearch.getText().trim();

    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

    // Jika sorter belum dibuat, buat sekarang
    if (jTable1.getRowSorter() == null) {
        sorter = new TableRowSorter<>(model);
        jTable1.setRowSorter(sorter);
    }

    // Jika kosong â†’ tampilkan semua
    if (searchText.isEmpty()) {
        sorter.setRowFilter(null);
        JOptionPane.showMessageDialog(this, "Pencarian dikosongkan. Semua data ditampilkan.");
        return;
    }

    // Filter semua kolom, tidak case-sensitive
    sorter.setRowFilter(RowFilter.regexFilter("(?i)" + searchText));

    JOptionPane.showMessageDialog(this, "Pencarian untuk: " + searchText);
    }//GEN-LAST:event_txtSearchActionPerformed

