    // ===================== FUNGSI AMBIL HARGA LEVEL =====================
    private int getHargaLevel(String level) {
        switch (level) {
            case "A1": return hargaA1;
            case "A2": return hargaA2;
            case "B1": return hargaB1;
            case "B2": return hargaB2;
            case "B3": return hargaB2; // ikut harga B2
            case "C1": return hargaC1;
            default: return 0;
        }
    }

    // ===================== FUNGSI AMBIL HARGA KELAS =====================
    private int getHargaJenisKelas(String kelas) {
        switch (kelas) {
            case "Reguler": return hargaReguler;
            case "Intensive": return hargaIntensive;
            case "Privat": return hargaPrivat;
            case "Weekend": return hargaWeekend;
            case "Online": return hargaOnline;
            default: return 0;
        }
    }

    // ===================== HITUNG TOTAL =====================
    private int hitungTotal(String level, String jenisKelas) {
        return getHargaLevel(level) + getHargaJenisKelas(jenisKelas);
    }
    
    // ===================== EVENT BUTTON SIMPAN =====================
    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {

        // === Ambil Nama ===
        String nama = txtNama.getText();

        // === Ambil Tanggal ===
        String tgl = comboTanggal.getSelectedItem().toString();
        String bln = comboBulan.getSelectedItem().toString();
        String thn = comboTahun.getSelectedItem().toString();

        String tanggal = tgl + " " + bln + " " + thn;

        // === Ambil Paket ===
        String paket = comboPaket.getSelectedItem().toString();

        // === Ambil Kelas ===
        String kelas = comboJenisKelas.getSelectedItem().toString();

        // === Ambil Level ===
        String level = "";
        if (rbA1.isSelected()) level = "A1";
        if (rbA2.isSelected()) level = "A2";
        if (rbB1.isSelected()) level = "B1";
        if (rbB2.isSelected()) level = "B2";
        if (rbB3.isSelected()) level = "B3";
        if (rbC1.isSelected()) level = "C1";

        // === Hitung Harga ===
        int total = hitungTotal(level, kelas);

        // === TAMBAHKAN KE TABLE ===
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{
            nama, tanggal, paket, kelas, level, total
        });

        JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
    }

